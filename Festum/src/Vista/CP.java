    public void cargar_estados(){
       ResultSet rs= CP.consultar_estados_C();
       cmbestado.removeAllItems();
        try {
            while(rs.next()){
                cmbestado.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(CP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
