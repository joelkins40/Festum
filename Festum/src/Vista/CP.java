     void llenar_tabla(String estado,String mun) {
        
        ResultSet rs=CP.consultar_colonias(estado, mun);
        
        try {
            while(rs.next()){
        DefaultTableModel tabladet = (DefaultTableModel) tbcolonias.getModel();
        String[] registros = new String[8];
               
                
                String dias = rs.getString("colonia");
                String diaArray[] = dias.split(";");
                
                for (String dia : diaArray) {
                    
                    
                    
                    
                    registros[0] = rs.getString(4);
                    registros[1] = rs.getString(3);
                    registros[2] = dia;
                    registros[3] =  rs.getString(1);
                    tabladet.addRow(registros);
                         tbcolonias.setModel(tabladet);
   
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(CP.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
     
